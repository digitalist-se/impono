namespace SevenTag\Api\ContainerBundle\Form;

use Symfony\Component\Form\AbstractType;
use Symfony\Component\Form\Extension\Core\Type\CollectionType;
use Symfony\Component\Form\FormBuilderInterface;
use Symfony\Component\OptionsResolver\OptionsResolver;

/**
 * Class ContainerWebsiteType
 * @package SevenTag\Api\ContainerBundle\Form
 */
class ContainerWebsiteType extends AbstractType
{
    /**
     * {@inheritdoc}
     */
    public function buildForm(FormBuilderInterface $builder, array $options)
    {
        $builder
            ->add('websites', CollectionType::class, [
                'entry_type' => 'seventag_container_website_type',
                'allow_add' => true,
                'allow_delete' => true,
                'by_reference'=> false
            ]);
    }

    /**
     * {@inheritdoc}
     */
    public function configureOptions(OptionsResolver $resolver)
    {
        $resolver->setDefaults([
            'data_class' => 'SevenTag\Api\ContainerBundle\Entity\Container'
        ]);
    }

    /**
     * {@inheritdoc}
     */
    public function getName()
    {
        return 'seventag_container_container_website_type';
    }
}
