/**
 * @name tagContainer.containerMock
 *
 * @param {$httpBackend} $httpBackend
 * @param {ngTableParams} ngTableParams
 * @param $filter
 * @param {$mockProvider} Mock
 */
var containerMock = ($httpBackend, ngTableParams, $filter, Mock) => {
    var containers = Mock.all('containers');

    var id = containers[containers.length - 1].id + 1;

    $httpBackend.whenGET(/\api\/containers\/(.*)\/permissions\?*/).respond(() => {
        return [200, {data: permissions}];
    });

    $httpBackend.whenGET(/\api\/containers\/(.*)/).respond((method, url) => {
        var matches = url.match(/\/api\/containers\/(.*)/);
        matches = matches[1].split('?');

        return [200, {data: Mock.get('containers', parseInt(matches[0]))}];
    });

    $httpBackend.whenDELETE(/\api\/containers\/(.*)/).respond((method, url) => {
        var matches = url.match(/\/api\/containers\/(.*)/);
        matches = matches[1].split('?');
        containers = Mock.remove('containers', parseInt(matches[0]));

        return [204];
    });

    $httpBackend.whenPOST(/\/api\/containers\/(.*)\/version-publish?(.*)/).respond(
        (method, url) => {
            var matches = url.match(/\/api\/containers\/(.*)\/version-publish/);
            matches = matches[1].split('?');

            var currentContainer = Mock.get('containers', parseInt(matches[0]));

            /* eslint-disable */
            currentContainer.has_unpublished_changes = false;
            currentContainer.published_at = new Date();
            /* eslint-enable */

            Mock.update('containers', currentContainer);
            Mock.copy();

            return [200, {data: currentContainer}];
        }
    );

    $httpBackend.whenPOST(/\/api\/containers\/(.*)\/version-restore?(.*)/).respond(
        (method, url) => {
            var matches = url.match(/\/api\/containers\/(.*)\/version-restore?(.*)/);
            matches = matches[1].split('?');

            var currentContainer = Mock.get('containers', parseInt(matches[0]));

            /* eslint-disable */
            currentContainer.has_unpublished_changes = false;
            currentContainer.published_at = new Date();
            /* eslint-enable */

            Mock.update('containers', currentContainer);
            Mock.restore();

            return [200, {data: currentContainer}];
        }
    );

    $httpBackend.whenGET(/\/api\/containers\?*/).respond((method, url, data) => {
        var query = url.split('?')[1],
            requestParams = {},
            params,
            vars = query.split('&');

        for (var i = 0; i < vars.length; i++) {
            var pair = vars[i].split('=');
            requestParams[decodeURIComponent(pair[0])] = decodeURIComponent(pair[1]);
        }

        // parse url params
        for (var key in requestParams) {
            if (key.indexOf('[') >= 0) {
                params = key.split(/\[(.*)\]/);
                var value = requestParams[key], lastKey = '';

                /* eslint-disable */
                angular.forEach(params.reverse(), (name) => {
                    if (name !== '') {
                        var v = value;
                        value = {};
                        value[lastKey = name] = !isNaN(v) ? parseFloat(v) : v;
                    }
                });
                /* eslint-enable */

                requestParams[lastKey] = angular.extend(requestParams[lastKey] || {}, value[lastKey]);
            } else {
                requestParams[key] = !isNaN(requestParams[key]) ? parseFloat(requestParams[key]) : requestParams[key];
            }
        }

        data = [];

        for (var containerIndex in containers) {
            if (containers[containerIndex].permissions.length > 0) {
                data.push(containers[containerIndex]);
            }
        }

        var total = data.length;

        /* eslint-disable */
        params = new ngTableParams({
            page: (requestParams.offset/requestParams.limit) + 1,
            count: requestParams.limit
        });
        /* eslint-enable */

        data = data.slice((params.page() - 1) * params.count(), params.page() * params.count());

        return [200, {
            data: data,
            total: total
        }];
    });

    $httpBackend.whenPOST(/\/api\/containers/).respond(
       (method, url, data) => {
           data = JSON.parse(data);
           /* eslint-disable */
           var newContainer = {
                id: parseInt(id),
                name: data.name,
                description: 'Default description',
                code: '<script>sample of code</script>',
                created_by: 2,
                created_at: '2015-04-14 09:50:28',
                updated_at: '2015-04-14 09:50:28',
                version: {
                    id: 120
                },
                has_unpublished_changes: false,
                published_at: false,
                delay: 1000
           };
           /* eslint-enable */

           Mock.add('containers', newContainer);
           id++;

           return [201, {data: newContainer}];
       }
    );

    $httpBackend.whenPUT(/\/api\/containers\/(.*)/).respond(
        (method, url, data) => {
            data = JSON.parse(data);

            var matches = url.match(/\/api\/containers\/(.*)/);
            matches = matches[1].split('?');

            var container = Mock.get('containers', parseInt(matches[0]));

            container.name = data.name;
            container.delay = data.delay;

            Mock.update('containers', container);

            return [200, {data: container}];
        }
    );
};

module.exports = {containerMock, containersList};
